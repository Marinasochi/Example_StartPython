username = input('Введите имя  ')      # Введите имя - присваивание переменной строки, приглашение к вводу
if username == 'Маша':                 # Если будет введено имя Маша, то ...
    print('Добро пожаловать Маша!')    # Напечатать такие слова
elif username == 'Марина':             # Если будет введено имя Марина, то ...
    print('Я так ждал Вас!')           # Напечатать такие слова
elif username == 'Василий':            # Если будет введено имя Василий, то ...
    print('Можете пройти, Василий!')   # Напечатать такие слова ...
else: print('Покажите пропуск!')       # Иначе напечатать такие слова ...

